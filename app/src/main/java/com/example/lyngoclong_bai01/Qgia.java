package com.example.lyngoclong_bai01;

public class Qgia {
    private String tenQG;
    private int danSo;

    public Qgia(String tenQG, int danSo) {
        this.tenQG = tenQG;
        this.danSo = danSo;
    }

    public String getTenQG() {
        return tenQG;
    }

    public void setTenQG(String tenQG) {
        this.tenQG = tenQG;
    }

    public int getDanSo() {
        return danSo;
    }

    public void setDanSo(int danSo) {
        this.danSo = danSo;
    }
}
